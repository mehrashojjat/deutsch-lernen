CSV_PATH = os.path.join(os.path.dirname(__file__), "../word_data/quiz_csv/a2.csv")

def main():
    with open(CSV_PATH, newline='', encoding='utf-8') as f:
        content = f.read()

    rows = list(csv.reader(io.StringIO(content)))
    header = rows[0]

    # Find the translation_uk column index
    uk_col = header.index("translation_uk")

    updated = 0
    for row in rows[1:]:
        try:
            row_id = int(row[0])
        except (ValueError, IndexError):
            continue

        if row_id in TRANSLATIONS:
            current_uk = row[uk_col].strip() if uk_col < len(row) else ""
            if not current_uk:
                # Extend row if necessary
                while len(row) <= uk_col:
                    row.append("")
                row[uk_col] = TRANSLATIONS[row_id]
                updated += 1
            else:
                print(f"  Row {row_id}: already has translation '{current_uk}', skipping")

    # Write back using the same quoting style
    out = io.StringIO()
    writer = csv.writer(out, lineterminator='\n')
    writer.writerows(rows)
    result = out.getvalue()

    with open(CSV_PATH, 'w', encoding='utf-8', newline='') as f:
        f.write(result)

    print(f"Done. Updated {updated} rows with Ukrainian translations.")

if __name__ == "__main__":
    main()
