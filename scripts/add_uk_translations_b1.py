def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    csv_path = os.path.join(script_dir, '..', 'word_data', 'quiz_csv', 'b1.csv')
    csv_path = os.path.normpath(csv_path)

    with open(csv_path, newline='', encoding='utf-8') as f:
        rows = list(csv.reader(f))

    header = rows[0]
    if 'translation_uk' not in header:
        header.append('translation_uk')
        uk_col = len(header) - 1
        # Pad all existing data rows to have the new column
        for i in range(1, len(rows)):
            rows[i].append('')
    else:
        uk_col = header.index('translation_uk')

    added = 0
    for row_idx, translation in TRANSLATIONS.items():
        if row_idx < len(rows):
            rows[row_idx][uk_col] = translation
            added += 1

    with open(csv_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerows(rows)

    print(f"Done. Added/updated {added} Ukrainian translations.")
    print(f"Ukrainian column index: {uk_col} ('{header[uk_col]}')")

if __name__ == '__main__':
    main()
