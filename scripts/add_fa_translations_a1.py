CSV_PATH = os.path.join(os.path.dirname(__file__), "../word_data/quiz_csv/a1.csv")


def main():
    with open(CSV_PATH, newline='', encoding='utf-8') as f:
        content = f.read()

    rows = list(csv.reader(io.StringIO(content)))
    if not rows:
        print("Empty CSV, nothing to do.")
        return

    header = rows[0]
    # Ensure translation_fa column exists
    if "translation_fa" not in header:
        header.append("translation_fa")

    fa_col = header.index("translation_fa")

    updated = 0
    for row in rows[1:]:
        try:
            row_id = int(row[0])
        except (ValueError, IndexError):
            continue

        # Extend row if necessary
        while len(row) <= fa_col:
            row.append("")

        current = row[fa_col].strip()
        if not current and row_id in TRANSLATIONS:
            row[fa_col] = TRANSLATIONS[row_id]
            updated += 1

    # Write back preserving CSV style
    out = io.StringIO()
    writer = csv.writer(out, lineterminator='\n')
    writer.writerows(rows)
    result = out.getvalue()

    with open(CSV_PATH, 'w', encoding='utf-8', newline='') as f:
        f.write(result)

    print(f"Done. Updated {updated} rows with Persian translations.")


if __name__ == "__main__":
    main()
