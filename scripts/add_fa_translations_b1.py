CSV_PATH = os.path.normpath(
    os.path.join(os.path.dirname(os.path.abspath(__file__)),
                 '..', 'word_data', 'quiz_csv', 'b1.csv')
)


def main():
    with open(CSV_PATH, newline='', encoding='utf-8') as f:
        rows = list(csv.reader(f))

    header = rows[0]

    if 'translation_fa' not in header:
        header.append('translation_fa')
        fa_col = len(header) - 1
        for row in rows[1:]:
            row.append('')
    else:
        fa_col = header.index('translation_fa')

    updated = 0
    for row in rows[1:]:
        try:
            row_id = int(row[0])
        except (ValueError, IndexError):
            continue

        while len(row) <= fa_col:
            row.append('')

        current = row[fa_col].strip()
        if not current and row_id in TRANSLATIONS:
            row[fa_col] = TRANSLATIONS[row_id]
            updated += 1

    out = io.StringIO()
    writer = csv.writer(out, lineterminator='\n')
    writer.writerows(rows)
    result = out.getvalue()

    with open(CSV_PATH, 'w', encoding='utf-8', newline='') as f:
        f.write(result)

    print(f"Done. Updated {updated} rows with Persian translations.")
    print(f"Persian column index: {fa_col} ('{header[fa_col]}')")


if __name__ == '__main__':
    main()
